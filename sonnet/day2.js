// switch statement

var day = "Friday";

switch(day){
	case "Monday":
		console.log("Boooooo!");
		break;
	case "Friday":
		console.log("Party!");
		break;	
	default:
		console.log("not Monday");	
}



if (day === "Monday"){
	console.log("Boooooo!")
} else if (day === "Friday"){
	console.log("Party!");
} else {
	console.log("Party!");	
}

